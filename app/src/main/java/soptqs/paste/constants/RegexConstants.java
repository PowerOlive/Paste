    public static Boolean isBilibiliAV(String str) {
        str = str.toLowerCase();
        Pattern pattern = Pattern.compile("av([0-9]{1,})");
        Matcher matcher = pattern.matcher(str);
        Log.e("match", "isBilibiliAV: " + matcher.matches());
        return matcher.matches();
    }

    public static String extractBilibiliAV(String str) {
        List<String> list = new ArrayList<>();
        StringBuilder av = new StringBuilder();
        str = str.toLowerCase();
        Pattern pattern = Pattern.compile("av([0-9]{1,})");
        Matcher matcher = pattern.matcher(str);
        while (matcher.find()) {
            list.add(matcher.group());
        }
        for (int i = 0; i < list.size(); i++) {
            av.append(list.get(i));
        }
        Log.e("extrace", "extractBilibiliAV: " + av);
        return av.toString();
    }

    public static Boolean isColorCode(String str) {
        str = str.toUpperCase();
        Pattern patternRGBSimple = Pattern.compile("^#[0-9A-F]{6}$");
        Pattern patternRGBSimple2 = Pattern.compile("^#[0-9A-F]{8}$");
        Matcher matcher1 = patternRGBSimple.matcher(str);
        Matcher matcher2 = patternRGBSimple2.matcher(str);
        return matcher1.matches() || matcher2.matches();
    }

    public static Boolean isBlocking(String string) {
        List<RegexSaves> regexSaves = DataSupport.findAll(RegexSaves.class);
        for (RegexSaves regexSaves1 : regexSaves) {
            if (regexSaves1.getEnable()) {
                try {
                    Pattern pattern = Pattern.compile(regexSaves1.getRegex());
                    Matcher matcher = pattern.matcher(string);
                    if (matcher.matches()) return true;
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        return false;
    }
